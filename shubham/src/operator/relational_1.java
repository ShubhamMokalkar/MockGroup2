package operator;

public class relational_1 {
	
	public static void main(String[] args) {
		
		int a =20, b=50;
		// value of a and b
		
		System.out.println("a="+20+"b="+30);
		
		// value == operator
		System.out.println(a==b);
		
		// ! = operator
		System.out.println(a!=b);
		
		// > operator
		System.out.println(a>b);
		
		//< operator
		System.out.println(a<b);
		
		// >= opearto
		System.out.println(a>=b);
		
		// <= operator
		System.out.println(a<=b);
		
	}

}
